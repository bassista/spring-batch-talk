DROP TABLE IF EXISTS SYMBOLS;

CREATE TABLE SYMBOLS  (
	ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL PRIMARY KEY,
	SYMBOL VARCHAR(5) NOT NULL,
	NAME VARCHAR(255) NOT NULL,
	INGESTED BOOLEAN NULL
);

DROP TABLE IF EXISTS QUOTES;

CREATE TABLE QUOTES  (
	ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL PRIMARY KEY,
	SYMBOL VARCHAR(5) NOT NULL,
	TIME DATE NOT NULL,
	HIGH DECIMAL(12,4) NULL,
	LOW DECIMAL(12,4) NULL,
	OPEN DECIMAL(12,4) NULL,
	CLOSE DECIMAL(12,4) NULL,
	VOLUME BIGINT NULL
);
